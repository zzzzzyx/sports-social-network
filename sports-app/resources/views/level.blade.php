				  <h4>初出茅庐</h4>
				  <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp这个阶段是调整你的身体状态的时候，不管你是增肌还是减脂，当你要开始健身运动的时候，相信你一定是已经有长时间没有运动了。你的身体是处于一个休眠的状态的，体能相对较差。这个时候，我们可以通过一些简单的运动，例如跑步、低强度的hiit等。一周保证4天的训练，你也可以练一天休息一天，练两天休息一天这样，坚持一个月。</p>
				  <p><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp一个月之后，你的体能会明显的变化，这也是你体能进步最快的时候，所以运动的人和不运动的人，还是会有明显的差距的。</p>
				  <p><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp适应期间运动不必高强度，我们的目的是为了让身体适应，提高你的心肺能力，每次有氧运动控制在40-60分钟，无氧运动控制在20分钟即可，胖子也可以暂时不做无氧运动。只做有氧。</p>
				  <br><h4>小有所成</h4>
				  <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp对于很多胖子来讲，把自己瘦下来，算是健身最大的目标了。经过一个月的适应，你也该为减脂做点事了。为了减掉身体的脂肪，我们需要做两件事:</p>
				  <p><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp1、控制饮食。尽量做到少盐，少油，少糖。主食和肉食适当的减少一些，一日三餐可以改成一日五餐，但每餐饮食的摄入量要减少一些。另外控制一下零食和饮料，最好不吃。其他的就没有必要控制了，自己在吃饭的时候，留意一下自己正在减肥期就可以了。</p>
				  <p><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp2、加大运动量。在经过了一个月的适应之后，你的体能应该有一个质的飞跃，这个时候可以提高一下强度，时间还是在40-60分钟，但是运动的强度可以调整一下，最简单的就是hiit运动，调整间歇的时间，或者调整动作的难度即可。</p>
				  <p><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp这个减脂期，时间长短每个人都不同，毕竟体质不同，减肥效果也不同。但只要坚持，你一定会瘦下来的。</p>
				  <br><h4>长驱深入</h4>
				  <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp在减脂成功后，我们则需要以力量训练为主，进入我们力量的提升阶段，也是肌肉的提升阶段，这个阶段大概是两个月时间。</p>
				  <p><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp我们在提升期间，可以调整力量训练的时间，提高到30-40分钟，并且可以给自己定制训练计划，细分肌肉群来锻炼，例如周一练胸肌，周二练腿部肌肉，周三练肩膀，周四休息，周五练背部肌肉等。</p>
				  <p><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp每天只针对一个或两个肌肉群，强度则不用太大，也不用觉得自己弱，毕竟我们才刚刚开始。如果你是先经历了减肥期的，这时候你也可以每次训练的时候，加入30分钟的有氧运动，以达到持续燃脂的目的。</p>
				  <p><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp饮食方面，提升期你要多吃蛋白质，逐步减少碳水化合物的量，做到低脂饮食。训练后建议增加一餐，补充蛋白质，主要目的就是防止肌肉的大量分解。你可以在训练后吃一些蛋白粉、鸡胸肉、鸡蛋白等，也可以额外补充一根香蕉或一片面包。</p>
				  <br><h4>健身无涯</h4>
				  <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp如果你能走到这一步，证明你的身材已经还不错了，至少也健身了3-5个月了。这个阶段的持续时间就要看你自己了，毕竟3-5个月下来，健身知识也看了不少了，你也会有自己的健身方式了。下面小hi也只是给出一些建议。</p>
				  <p><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp力量训练保持在30-40分钟左右，组间的休息尽量控制在一分钟以内，多选用复合动作来练遍全身的肌肉，而不是那些绣花一样的单关节动作。</p>
				  <p><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp你的训练计划中，一定要练到全身的肌肉，包括让很多人害怕的腿部肌肉和肩膀肌肉。深蹲，卧推，硬拉这些动作对于提高力量和增长肌肉很有好处。</p>
				  <p><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp每周要进行2次的有氧运动，以保证你的心肺能力，这样做的目的也是可以让你继续燃脂。</p>
				  <p><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp最后，就是坚持，坚持，再坚持了。每个人的身体体质不同，要经历每个阶段的时间也会不同，但自己最了解自己，健身需要慢慢来，不要操之过急，让身体保持一个舒服到不舒服的界线就可以了，慢慢的提升，达到自己想要的身材。</p>
